"""Base Tecnica de Computos OBYRA.

Diccionario estructurado de reglas tecnicas para reconocer items de obra
durante el analisis IA de presupuestos. Sirve como referencia interna
para clasificar:
  - Rubro y etapa estandar.
  - Unidad de medida esperada y criterio de medicion.
  - Materiales, mano de obra y maquinaria asociados.
  - Rendimiento y desperdicio orientativos.
  - Confianza base.

DISCLAIMER:
  Las reglas estan inspiradas en bibliografia tecnica clasica de la
  construccion argentina (criterios generales de computos y presupuestos),
  pero los valores especificos (rendimientos, desperdicios, listas de
  materiales) son criterios internos de OBYRA, simplificados y editables.
  No se reproducen textos literales ni fragmentos extensos de obras
  protegidas por derechos de autor.

Estructura de cada regla:
    'id'                    : str unico (slug)
    'rubro'                 : str legible (ver RUBROS_RECONOCIDOS)
    'etapa'                 : nombre de etapa estandar (compatible con etapa_matcher)
    'tarea'                 : descripcion canonica de la tarea
    'unidad_esperada'       : codigo de unidad (m2, m3, m, kg, etc.)
    'unidades_validas'      : lista de unidades aceptadas como sinonimos
    'criterio_medicion'     : texto breve de como se mide
    'palabras_clave_fuertes': matches >= 1 -> alta confianza
    'palabras_clave_medias' : matches >= 1 -> media confianza
    'palabras_clave_debiles': matches >= 1 -> baja confianza
    'palabras_excluyentes'  : si aparece alguna -> NO match (ej: "demolicion de muro" no es muro nuevo)
    'materiales_sugeridos'  : list[str]
    'mano_obra_sugerida'    : list[str]
    'maquinaria_sugerida'   : list[str]
    'rendimiento_estimado'  : texto orientativo
    'desperdicio_estimado'  : porcentaje orientativo
    'observaciones_tecnicas': texto breve
    'fuente'                : siempre 'criterio interno OBYRA'
"""
