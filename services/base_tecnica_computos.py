def reglas_por_rubro(rubro: str) -> list:
    """Devuelve las reglas que pertenecen a un rubro dado."""
    return [r for r in REGLAS_TECNICAS if r.get('rubro') == rubro]


def regla_por_id(regla_id: str) -> dict | None:
    """Busca una regla por su id."""
    for r in REGLAS_TECNICAS:
        if r.get('id') == regla_id:
            return r
    return None


def total_reglas() -> int:
    return len(REGLAS_TECNICAS)
